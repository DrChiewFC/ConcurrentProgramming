package javafxapplication1;

/**
 *
 * @author Acer
 */
public class Points {
    float xPoint;
	float yPoint;
	
	public Points() {
		
	}
	
	public Points(float xPoint, float yPoint) {
		this.xPoint = xPoint;
		this.yPoint = yPoint;
	}
	
	public float getxPoint() {
		return xPoint;
	}
	public void setxPoint(float xPoint) {
		this.xPoint = xPoint;
	}
	public float getyPoint() {
		return yPoint;
	}
	public void setyPoint(float yPoint) {
		this.yPoint = yPoint;
	}
	

}
